    private void getBranchFile() {
        Db_branchfile branch = new Db_branchfile();
        List list = branch.getBranchFile();
        int cnt = list.size();
        for (int i = 0; i < cnt; i++) {
            Branchfile bf = (Branchfile) list.get(i);
            String[] data = {bf.getBranchCode(), ThaiUtil.ASCII2Unicode(bf.getBranchName()), 
                bf.getTelNo(), bf.getFaxNo(), ThaiUtil.ASCII2Unicode(bf.getManagerName())};
            model.addRow(data);
        }
    }

