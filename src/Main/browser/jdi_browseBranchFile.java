private void btn_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_okActionPerformed
// TODO add your handling code here:
    int row = tblbranchdata.getSelectedRow();
    if (row < 0) {
        JOptionPane.showMessageDialog(this, "ไม่มีข้อมูลที่เลือกกรุณาตรวจสอบข้อมูล", "Mycps", JOptionPane.OK_OPTION);
    } else {
        String branchCode = model.getValueAt(row, 0).toString();
        branchSelect = branchCode;
        dispose();
    }

}//GEN-LAST:event_btn_okActionPerformed

