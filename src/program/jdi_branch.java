    public void getDataToForm(String branchCode) {
        
        Db_branchfile branch = new Db_branchfile();
        Branchfile bean = (Branchfile) branch.getBranchFile(branchCode);
        txtbranchCode.setEditable(false);
        //btn_find.setEnabled(false);
        if (bean != null) {
            btn_find.setEnabled(false);
            txtbranchCode.setText(bean.getBranchCode());
            txtbranchName.setText(bean.getBranchName());
            txtbranchDisTric.setText(bean.getSubProvince());
            txtbranchEmail.setText(bean.getEmail());
            txtbranchFax.setText(bean.getFaxNo());
            txtbranchManager.setText(bean.getManagerName());
            txtAdressNo.setText(bean.getAddress());
            txtbranchPhone.setText(bean.getTelNo());
            txtbranchPost.setText(bean.getPost());
            txtbranchProvince.setText(bean.getProvince());
            txtbranchSubdisTrict.setText(bean.getLocality());
            
            totArea.setValue(bean.getTot_Area());
            couArea.setValue(bean.getCou_Area());
            kicArea.setValue(bean.getKic_Area());
            charge.setValue(bean.getCharge());
            serArea.setValue(bean.getTot_Area());
            gP.setValue(bean.getGp());
            cost.setValue(bean.getCost());
            
            totArea.setText(doubleFmt.format(bean.getTot_Area()));
            couArea.setText(doubleFmt.format(bean.getCou_Area()));
            kicArea.setText(doubleFmt.format(bean.getKic_Area()));
            serArea.setText(doubleFmt.format(bean.getTot_Area()));
            charge.setText(doubleFmt.format(bean.getCharge()));
            gP.setText(bean.getGp().toString());
            cost.setText(doubleFmt.format(bean.getCost()));

            if (bean.getFlageGp().equals("Y")) {
                chGp.setSelected(true);
            } else {
                chGp.setSelected(false);
            }

            if (bean.getFlageCost().equals("Y")) {
                chService.setSelected(true);
            } else {
                chService.setSelected(false);
            }

            btn_remove.setEnabled(true);
        }

    }

