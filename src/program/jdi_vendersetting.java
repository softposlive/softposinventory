private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
// TODO add your handling code here:
    txtName.requestFocus();
    
    if (txtCode.getText().trim().length() > 4 || txtCode.getText().trim().length() < 4) {
        JOptionPane.showMessageDialog(this, "รหัสผู้จำหน่ายไม่ถูกต้อง กรุณาระบุรหัสผู้จำหน่ายจำนวน 4 หลัก", "Mycps", JOptionPane.WARNING_MESSAGE);
        txtCode.requestFocus();
    } else if (txtName.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(this, "กรุณาระบุชื่อชื่อจำหน่าย", "Mycps", JOptionPane.WARNING_MESSAGE);
        txtName.requestFocus();
    } else if (txtPost.getText().trim().length() > 0 && txtPost.getText().trim().length() < 5) {
        JOptionPane.showMessageDialog(this, "รหัสไปรษณีย์ไม่ถูกต้อง", "Mycps", JOptionPane.WARNING_MESSAGE);
        txtPost.requestFocus();
    } else if (txtPost.getText().trim().length() > 5) {
        JOptionPane.showMessageDialog(this, "รหัสไปรษณีย์ไม่ถูกต้อง", "Mycps", JOptionPane.WARNING_MESSAGE);
        txtPost.requestFocus();
    } else if (txtSendTime.getText().trim().length() == 0) {
        JOptionPane.showMessageDialog(this, "กรุณาระบุระยะเวลาการส่งสินค้า", "Mycps", JOptionPane.WARNING_MESSAGE);
        txtSendTime.requestFocus();
    } else { 
        Db_vender vender = new Db_vender();
        Vender bean = new Vender();
        bean.setVname(txtName.getText());
        bean.setVaddress(txtAdressNo.getText());
        bean.setVCity(txtCity.getText());
        bean.setVSubProvince(txtSubprovince.getText());
        bean.setVProvince(txtProvince.getText());
        bean.setVPost(txtPost.getText());
        bean.setVTel(txtPhone.getText());
        bean.setVFax(txtFax.getText());
        bean.setVEmail(txtEmail.getText());
        bean.setVContack(txtContack.getText());
        bean.setVDept(txtDep.getText());
        try {
            int time = Integer.parseInt(txtSendTime.getValue().toString());
            if (time <= 0) {
                bean.setVSendTime("0");
            } else {
                bean.setVSendTime(txtSendTime.getValue().toString());
            }
        } catch (Exception e) {
            bean.setVSendTime("0");
        }
        bean.setVConPur(txtVConPur.getText());
        bean.setVTran(txtVTran.getText());
        bean.setVBusType(txtTypeBus.getText());
        bean.setVBusAssi(txtVbusAssi.getValue().toString());
        bean.setVBusBegin(txtDate.getText());
        bean.setVBusNo(txtVBusNo.getText());
        bean.setVTaxNo(txtVTaxNo.getText());
        bean.setVCrTerm(txtCrTerm.getText());
        try {
            Float amout = Float.parseFloat(txtVCrAmount.getValue().toString());
            if (amout <= 0) {
                bean.setVCrAmount("0.00");
            } else {
                bean.setVCrAmount(txtVCrAmount.getValue().toString());
            }
        } catch (Exception e) {
             bean.setVCrAmount("0.00");
        }

        bean.setVPayType(txtpayType.getText());
        bean.setVBankAcc(txtbk.getText());
        bean.setVDiscount(txtDiscount.getText());
        bean.setVCharge(txtCharge.getText());
        bean.setVRemark(txtRemark.getText());
        bean.setVCode(txtCode.getText());
        String mode = "";
        if (txtDisplayMode.getText().trim().equals("Update Mode")) {
            mode = "update";
            if (vender.inSertVender(bean, mode)) {
                JOptionPane.showMessageDialog(this, "แก้ไขข้อมูลผู้จำหน่ายแล้ว");
                clearForm();
            }
        } else {
            mode = "insert";
            if (vender.inSertVender(bean, mode)) {
                JOptionPane.showMessageDialog(this, "บันทึกข้อมูลผู้จำหน่ายแล้ว");
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "เกิดความขัดข้องของระบบไม่สามารถบันทึกข้อมูลได้");
            }
        }

    }


}//GEN-LAST:event_btn_saveActionPerformed

private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
// TODO add your handling code here:
    Db_vender vender = new Db_vender();
    int confirm = JOptionPane.showConfirmDialog(this, "คุณต้องการลบข้อมูลรายการนี้หรือไม่...?", "Confirm", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        if (vender.removeVenderFile(txtCode.getText())) {
            JOptionPane.showMessageDialog(this, "ระบบลบข้อมูลผู้จำหน่าย" + txtName.getText() + "แล้ว");
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "ระบบไม่สามารถลบข้อมูลผู้จำหน่าย " + txtName.getText() + " ได้ เนื่องจากมีการนำไปใช้งานแล้ว", "Mycps", JOptionPane.ERROR_MESSAGE);
        }
    }

}//GEN-LAST:event_btn_removeActionPerformed

