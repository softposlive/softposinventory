    public void getDataToForm(String code) {
        Db_vender vender = new Db_vender();
        Vender bean = (Vender) vender.getVenderFile(code);
        if (bean != null) {
            checkNameClick = 1;
            txtDisplayMode.setText("Update Mode");
            txtAdressNo.setText(bean.getVaddress());
            txtCharge.setText(bean.getVCharge());
            txtContack.setText(bean.getVContack());
            txtCrTerm.setText(bean.getVCrTerm());
            Date time;
            String date = "";
            try {
                time = enFmt.parse(bean.getVBusBegin());
                date = thFmt.format(time);
                txtDate.setText(date);
            } catch (Exception ex) {
                txtDate.setText("");
            }

            txtDep.setText(bean.getVDept());
            txtDiscount.setText(bean.getVDiscount());
            txtProvince.setText(bean.getVProvince());
            txtEmail.setText(bean.getVEmail());
            txtFax.setText(bean.getVFax());
            txtPhone.setText(bean.getVTel());
            txtPost.setText(bean.getVPost());
            txtCity.setText(bean.getVCity());
            txtRemark.setText(bean.getVRemark());
            txtSendTime.setText(bean.getVSendTime());
            txtSubprovince.setText(bean.getVSubProvince());
            txtTypeBus.setText(bean.getVBusType());
            txtVBusNo.setText(bean.getVBusNo());
            txtVConPur.setText(bean.getVConPur());
            try {
                Float am = Float.parseFloat(bean.getVCrAmount());
                txtVCrAmount.setText(doubleFmt.format(am));
                txtVCrAmount.setValue(am);
            } catch (Exception e) {
                txtVCrAmount.setText("0.00");
            }

            txtVTaxNo.setText(bean.getVTaxNo());
            txtVTran.setText(bean.getVTran());
            try {
                Float bus = Float.parseFloat(bean.getVBusAssi());
                txtVbusAssi.setText(doubleFmt.format(bus));
                txtVbusAssi.setValue(bus);
            } catch (Exception e) {
                txtVbusAssi.setText("0.00");
            }

            txtbk.setText(bean.getVBankAcc());
            txtCode.setText(bean.getVCode());
            txtName.setText(bean.getVname());
            txtpayType.setText(bean.getVPayType());
            txtCode.setEditable(false);
            btn_remove.setEnabled(true);
        } else {
            txtCode.setEditable(false);
            txtDisplayMode.setText("Insert Mode");
        }

    }

